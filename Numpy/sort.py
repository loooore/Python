import numpy as np

# 1-D sort
np1 = np.array([6, 7, 0, 4, 9, 2, 1])
print(np.sort(np1))

# 2-D sort
np2 = np.array([[1, 2, 4, 65, 0], [1, 4, 324, 2, 1]])
np.sort(np2)

# Alphabetical
np3 = np.array(['Lore', 'Tinna', 'Aaron'])
print(np.sort(np3))

# Boolean
np4 = np.array([True, False, True])
print(np.sort(np4))

